import UIKit

extension UIColor {
    
    @nonobjc class var backgroundPrimary: UIColor {
        return UIColor(named: "BackgroundPrimary")!
    }
    
    @nonobjc class var backgroundSecondary: UIColor {
        return UIColor(named: "BackgroundSecondary")!
    }
    
    @nonobjc class var error: UIColor {
        return UIColor(named: "Error")!
    }
    
    @nonobjc class var info: UIColor {
        return UIColor(named: "Info")!
    }
    
    @nonobjc class var primary: UIColor {
        return UIColor(named: "Primary")!
    }
    
    @nonobjc class var secondary: UIColor {
        return UIColor(named: "Secondary")!
    }
    
    @nonobjc class var textPrimary: UIColor {
        return UIColor(named: "TextPrimary")!
    }
    
    @nonobjc class var textSecondary: UIColor {
        return UIColor(named: "TextSecondary")!
    }
    
    @nonobjc class var warning: UIColor {
        return UIColor(named: "Warning")!
    }
    
}
