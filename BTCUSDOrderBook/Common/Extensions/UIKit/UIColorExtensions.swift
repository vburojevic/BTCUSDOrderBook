import UIKit

extension UIColor {
    
    @nonobjc class var backgroundPrimary: UIColor {
        return UIColor(named: "BackgroundPrimary")!
    }
    
    @nonobjc class var themePrimary: UIColor {
        return UIColor(named: "ThemePrimary")!
    }
    
    @nonobjc class var themeSecondary: UIColor {
        return UIColor(named: "ThemeSecondary")!
    }
    
}
